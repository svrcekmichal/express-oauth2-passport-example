import mongoose, { Schema } from 'mongoose';

const Client = new Schema({
    name: {
        type: String,
        unique: true,
        required: true
    },
    clientId: {
        type: String,
        unique: true,
        required: true
    },
    clientSecret: {
        type: String,
        required: true
    }
});

export default mongoose.model('auth_client', Client);